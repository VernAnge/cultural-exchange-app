export function useRootNavigation() {
  const router = useRouter();

  useEffect(() => {
    const isAuthenticated = checkAuthenticationStatus();

    if (isAuthenticated) {
      router.replace('/tabs');
    } else {
      router.replace('/auth');
    }
  }, []);

  return null;
}